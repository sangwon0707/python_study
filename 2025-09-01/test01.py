# 변수에 숫자 계산 결과를 저장합니다.
a = 5 + 6

# 변수에 저장된 값을 다른 문자열과 함께 출력합니다.
print("5와 6의 합:", a)

# 변수를 사용하지 않고 계산 결과를 바로 출력할 수도 있습니다.
print("5와 6의 합:", 5 + 6)

# 아래 세 줄은 코드의 띄어쓰기(공백)는 다르지만, 모두 같은 결과가 출력됩니다.
# 파이썬은 코드의 인수(argument) 사이의 불필요한 공백을 무시하기 때문입니다.
print("아침","점심","저녁")
print("아침", "점심", "저녁")
print("아침",  "점심",  "저녁")

# sep 파라미터를 사용하면 값들 사이에 들어갈 구분자(separator)를 지정할 수 있습니다.
# 기본값은 공백(' ')이지만, 여기서는 '#'으로 변경했습니다.
print("아침","점심","저녁", sep="#")