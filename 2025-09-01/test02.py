# 변수 a에 문자열 "길동"을 저장합니다.
a = "길동"
# print() 함수는 기본적으로 출력 끝에 줄바꿈(엔터)을 추가합니다.
print("안녕", a)
print("반값다", a) 
print("오랜만이야", a)
print("-----------------------------------------------")

# end 파라미터를 사용하면 print() 함수가 끝날 때 출력할 문자를 지정할 수 있습니다.
# 기본값인 줄바꿈 대신 '-' 문자를 사용합니다.
a = "길동"
print("안녕", a, end="-")
print("반값다", a, end="-") 
print("오랜만이야", a, end="-")

# 위 print 문들은 줄바꿈을 하지 않으므로, 여기서 강제로 줄바꿈을 해줍니다.
print("\n")
print("-----------------------------------------------")

# print() 함수 한 번으로 여러 줄을 출력하는 방법입니다.
# \n (줄바꿈 문자)를 문자열 안에 직접 넣어주고, sep=""로 값 사이의 기본 공백을 없앴습니다.
a = "길동"
print("안녕", a, "\n", "반갑다", a, "\n", "오랜만이야", a, sep="")

# \t (탭 문자)를 사용하면 일정한 간격으로 띄어쓰기를 할 수 있어, 내용을 정렬할 때 유용합니다.
print("철수", "\t", "영희")
print("길동", "\t", "길동")


