# Python의 다양한 산술 연산자 (Arithmetic Operators)

# + (더하기): 두 숫자를 더합니다.
# 예: 10 + 3 = 13

# - (빼기): 첫 번째 숫자에서 두 번째 숫자를 뺍니다.
# 예: 10 - 3 = 7

# * (곱하기): 두 숫자를 곱합니다.
# 예: 10 * 3 = 30

# / (나누기): 첫 번째 숫자를 두 번째 숫자로 나눕니다. (결과는 항상 실수(float)가 됩니다.)
# 예: 10 / 3 = 3.333...

# // (정수 나누기 - 몫): 나눈 후, 소수점 이하를 버리고 정수 몫만 구합니다.
# 예: 10 // 3 = 3

# % (정수 나누기 - 나머지): 나눈 후, 남는 나머지를 구합니다. (모듈로 연산)
# 예: 10 % 3 = 1

# ** (거듭제곱): 첫 번째 숫자를 두 번째 숫자만큼 제곱합니다.
# 예: 10 ** 3 = 1000


#
# Python의 복합 대입 연산자 (Compound Assignment Operators)
# 기존 연산과 대입(=)을 한 번에 수행하여 코드를 간결하게 만듭니다.
#
# 아래 모든 예시는 a = 10 이라고 가정하고 개별적으로 실행한 결과입니다.
#
# a += 3  # a = a + 3 과 동일. 결과: a는 13
#
# a -= 3  # a = a - 3 과 동일. 결과: a는 7
#
# a *= 3  # a = a * 3 과 동일. 결과: a는 30
#
# a /= 3  # a = a / 3 과 동일. 결과: a는 3.333...
#
# a //= 3 # a = a // 3 과 동일. 결과: a는 3
#
# a %= 3  # a = a % 3 과 동일. 결과: a는 1
#
# a **= 3 # a = a ** 3 과 동일. 결과: a는 1000

